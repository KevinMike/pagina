@section('contenido')
<h2>NOSOTROS</h2>
<ol>
	<li><h4>¿POR QUÉ ESPECIALIZARSE EN LA ESCUELA NACIONAL DE ESTADÍSTICA E INFORMÁTICA DEL INEI?</h4></li>
	<p>Las sociedades modernas exigen a la educación, respuestas rápidas y efectivas no sólo para cumplir sus propios fines sino, para atender los problemas del contexto social, político, económico, etc.</p>
	<br>
	<p>Es en ese marco que, la Escuela Nacional de Estadística e Informática (ENEI) ofrece esta posibilidad de enseñanzas basadas en el uso de información intensiva , mediante cursos que buscan acercar a los profesionales y técnicos, de los diversos organismos e instituciones a un nivel competitivo en el mercado laboral, principalmente, en las metodologías estadísticas, buenas prácticas y uso de base de datos oficial y/o actualizada, con utilización de softwares estadísticos. </p>
	<br>
	<li><h4>La Estadística</h4></li>
	<p>La Estadística emplea diversas herramientas que proporcionan la Matemática y la Computación. Sin embargo, la innovación estadística no necesariamente envuelve innovación matemática o computacional, sino que, también, presenta desarrollos propios que son usados para el progreso de sí misma y en aplicaciones reales. Todo esto convierte a la Estadística en una ciencia propia e independiente.</p>
	<br>
	<p>Existe una demanda creciente en el uso de métodos estadísticos complejos para analizar grandes cantidades de datos que surgen en entidades de gobierno, negocios, industrias y ciencias, determinándose la necesidad de realizar un entrenamiento en el Perú, en un nivel intermedio y avanzado, de profesionales que puedan abordar estos desafíos.</p>
	<br>
	<p>La ENEI, ha elaborado el Plan de Capacitación de modo que sea flexible y apropiado para estudiantes de diferentes especialidades, tales como Estadística, Ingeniería (Informática, Electrónica, Industrial, Sistemas, Civil, etc.), Ciencias Médicas, Economía, Administración, Física, Matemática, Química, y otras especialidades afines, además del público en general.</p>
</ol>
 
<h2>MISION Y VISION</h2>
<ol>
<li><h4>MISION</h4></li>
<p>Proporcionar servicios de capacitación especializada en Estadística e Informática a los trabajadores del INEI, del Sistema Estadístico Nacional y público en general, incidiendo en la mejora e innovación de los procesos de trabajo y de gestión, para la obtención de productos y servicios de calidad, elevación de la cultura estadística, así como, promover la investigación y el intercambio conceptual y metodológico, con organismos nacionales e internacionales.</p>
<br>
<li><h4>VISIÓN</h4></li>
<p>La Escuela Nacional de Estadística e Informática ENEI, tiene como finalidad ser un órgano moderno, con infraestructura adecuada, competitiva, promotora del aprendizaje continuo y permanente, con reconocido prestigio internacional, orientada a satisfacer las necesidades de capacitación de nuestros usuarios, internos y externos, incorporando nuevos enfoques de metodologías estadísticas, de administración y tecnologías modernas de información y comunicaciones.</p>
</ol>
